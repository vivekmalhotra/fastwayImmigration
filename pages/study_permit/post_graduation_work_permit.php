                <li>they are on an implied status, meaning they submitted an application to extend or change their status to visitor or student before the expiry date of their study permit and no decision has been made</li>
            </ul>

            <h4 class="font-size-4 color-font-black py-2"><strong>Applicants whose study permit has become invalid or expires must either :</strong></h4>
