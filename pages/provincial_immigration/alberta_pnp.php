            <p class="font-size-4 py-1">
                Skilled and semi-skilled immigrants can apply for Alberta PNP 2020 and there are also options for immigrants to apply with or without any employer support. Alberta PNP is mainly broken into three main immigration streams :
            </p>

            <p class="font-size-4 py-1">
                <strong>Alberta Opportunity Stream</strong> – This stream is for the immigrants who can meet the streams requirements and are working in Alberta in an eligible occupation.  Alberta Opportunity Stream has replaced AINP’s Strategic Recruitment and Employer-Driven streams from June 14, 2018.
            </p>

            <p class="font-size-4 py-1">
                <strong>Alberta Express Entry Stream</strong> – In this Stream, Province searches for the capable immigrants in the Federal Government’s Express Entry Pool. This stream allows the express entry registered candidates to apply for provincial nomination. Once Provincial Nomination Certificate is issued, it adds 600 CRS points in Express Entry Profile of the respective client thus guaranteeing ITA in next Express Entry Draw.
            </p>

            <p class="font-size-4 py-1">
                <strong>Alberta Self-Employed Farmer Stream</strong> – This stream of AINP’s is to allow the immigrants with farming experience and personal funds to purchase and develop a farm in the province to apply for permanent residence. Candidates whose application will be successfully processed will be able to live in the province in order to purchase and manage their farming business
            </p>

            <p class="font-size-4 py-1">
                <strong>Alberta Opportunity Stream</strong> - This stream provides a pathway to Canada PR to applicants working in Alberta and international graduates who have completed their studies at a recognized Alberta educational post-secondary institution.
            </p>

            <p class="font-size-4 py-1">
                Every AINP Stream has different requirements.
            </p>

            <h4 class="font-size-4 color-font-black"><strong>Eligibility</strong></h4>
            <p class="font-size-4 py-1">
                Residency Status and Work Permit Requirements that a Candidate must have:
            </p>

            <ul class="font-size-4">
                <li>Valid temporary status authorizing to work in Canada</li>
                <li>A valid work permit (not implied status or restoration status) for an eligible occupation based on either:
                    <ul>
                        <li>A Positive Labour Market Impact Assessment (LMIA)</li>
                        <li>An LMIA exemption for workers</li>
                        <li>An LMIA exemption as a Post-Graduation Work Permit (PGWP) holder</li>
                    </ul>
                </li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Language Proficiency</strong></h4>
            <p class="font-size-4 py-1">
                The minimum requirement for language proficiency under this stream is to have Canadian Language Benchmark (CLB) level 4. It can be in either language whether it is in English or French. Candidates have to prove their knowledge of the language in four sections – Reading, Writing, Speaking and Listening.
            </p>

            <h4 class="font-size-4 color-font-black"><strong>Education requirements</strong></h4>
            <p class="font-size-4 py-1">
                At the time of applying under this stream, the candidate needs to have completed at least a Canadian high school or its equivalent in their country of the region.  The foreign education needs to be verified through an Educational Credential Assessment (ECA).
            </p>

            <h4 class="font-size-4 color-font-black"><strong>Work Experience</strong></h4>
            <p class="font-size-4 py-1">
                Candidates must:
            </p>
            <ul class="font-size-4">
                <li>Have a minimum of 12 months of work experience for a “current occupation” in Alberta within the last 18 months. OR</li>
                <li>Have a minimum of 24 months of work experience in “current occupation” in Canada and/or outside of Canada within the last 30 months.</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Occupation requirements</strong></h4>
            <p class="font-size-4 py-1">
                For an eligible occupation, at the time of AINP assess the application, the candidate must:
            </p>
            <ul class="font-size-4">
                <li>Work in an eligible occupation in the province</li>
                <li>Work in an occupation that matches the previous work experience</li>
            </ul>


            <h4 class="font-size-4 color-font-black"><strong>Express entry process of Alberta immigration nominee program (AINP)</strong></h4>
            <p class="font-size-4 py-1">
                This stream allows AINP to nominate skilled and capable immigrants from the federal government’s express entry pool.  For express entry candidates to apply under AINP, they must be invited by the province to do so. To get invited, the candidate must show strong ties to the province or can be the one that will support the province’s economic growth and diversification priorities. International graduates from Canadian post-secondary institutions can also be issued Notification of Interest under this stream.
            </p>

            <h4 class="font-size-4 color-font-black"><strong>Getting Notification of Interest from AINP under Express Entry</strong></h4>
            <p class="font-size-4 py-1">
                Based on the credentials submitted by the candidate to the AINP, the province can select the candidate and send them a Notification of Interest (NOI). In order to get an NOI letter, the candidate:
            </p>
            <ul class="font-size-4">
                <li>Must have an active express entry profile</li>
                <li>Must show an intention to immigrate to province and settle there permanently</li>
                <li>Must be working in an occupation that supports Alberta’s economic development and diversification, and</li>
                <li>Must have minimum <strong>Comprehensive Ranking System</strong> (CRS) score of 300 points</li>
            </ul>

            <p class="font-size-4 py-1">
                In case the candidate is able to follow one or more of the following adaptability factors, it will increase the likelihood of getting an NOI:
            </p>
            <ul class="font-size-4">
                <li><strong>Alberta job offer and/or work experience</strong></li>
                <li><strong>A graduate of a Canadian post-secondary institution</strong></li>
                <li><strong>A relative that is living in the province: parent, child, brother and/or sister</strong></li>
            </ul>

            <p class="font-size-4 py-1">
                Once the candidate’s application is approved, they will receive a nomination certificate via the IRCC Express entry portal. Candidate must accept the nomination within 30 days after it is issued in order to receive an Invitation to Apply (ITA).
            </p>

            <p class="font-size-4 py-1">
                After getting an ITA, the candidate has 60 days to submit their application for a permanent residence visa to the IRCC.
            </p>

            <p class="font-size-4 py-1">
                Alberta does not specify occupation in-demand list.  There are some occupations which are in demand in Alberta.   Candidates falling in any of the following categories, their chances will increase for getting a nomination from the province:
            </p>

            <ul class="font-size-4">
                <li>Trades, Transport, and Equipment Operators</li>
                <li>Unique to the Primary Industry (oil and gas)</li>
                <li>Sales and Services</li>
                <li>Business, Finance, and Administrative</li>
                <li>Management</li>
