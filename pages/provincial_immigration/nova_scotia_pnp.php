        <div class="container py-3 text-justify">
            <h2 class="font-size-2 color-font-black py-2"><strong>What is Nova Scotia PNP?</strong></h2>
            <p class="font-size-4 py-1">
                Nova Scotia Provincial Nominee Program (NS PNP) allows the province to nominate the prospective immigrants for permanent resident status in the province with skill sets and work experience that is targeted by the Nova Scotia’s local labor market.
            </p>

            <h2 class="font-size-2 color-font-black py-2"><strong>What are the different stream under Nova Scotia PNP process 2019?</strong></h2>
            <p class="font-size-4 py-1">
                Nova Scotia Provincial Nominee Program accepts applications for the following immigration streams:
            </p>
            <ul class="font-size-4">
                <li>Nova Scotia Demand: Express Entry</li>
                <li>Nova Scotia Experience: Express Entry</li>
                <li>Nova Scotia Labour Market Priorities</li>
                <li>Skilled Worker Stream</li>
                <li>Entrepreneur Stream</li>
                <li>International Graduate Entrepreneur Stream</li>
                <li>Physician Stream</li>
            </ul>
            <h4 class="font-size-4 color-font-black"><strong>Nova Scotia Demand : Express Entry</strong></h4>
            <p class="font-size-4 py-1">
                This stream uses the federal government’s express entry program selection system to select the candidates for provincial nomination. Highly skilled immigrants are selected who wants to live in the province as a permanent resident. There are two categories under this stream:
            </p>
            <ul class="font-size-4">
                <li>Category A – for applicants with a job offer</li>
                <li>Category B – Applicants with experience in opportunity occupation</li><span class="badge badge-danger">Category B has been closed by NSNP</span>
            </ul>

            <p class="font-size-4"><strong>Following are the eligibility criteria to apply to Category A :</strong></p>
            <ul class="font-size-4">
                <li>Must have profile registered in Express entry program</li>
                <li>Score minimum of 67 points on the stream’s six selection factors</li>
                <li>Must have a job offer from an eligible employer of province for a full-time skilled work that lasts at least one year after candidate’s permanent resident visa is issued</li>
                <li>Must have 1 year of skilled work experience related to the job</li>
                <li>For educational qualification, must have a Canadian high school credential or equivalent</li>
                <li>For Language proficiency, minimum Canadian Language Benchmark (CLB) level 7</li>
                <li>Must possess enough financial resources to settle in the province successfully</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Nova Scotia Experience : Express Entry</strong></h4>
            <p class="font-size-4 py-1">
                Province uses this stream to select candidates for this stream from federal government’s express entry program, to provide an immigration pathway to permanent resident for skilled immigrants who have worked for a province’s employer for a minimum of one year. In order to apply candidate must:
            </p>
            <ul class="font-size-4">
                <li>Have at least 1 year of work experience in Nova Scotia</li>
                <li>Age group must be between 21 to 55 years old</li>
                <li>Have a Canadian high school credential or equivalent</li>
                <li>Prove language ability in English or French on CLB level 7 minimum for NOC skill level 0 and A. For NOC skill level B, CLB 4 is the minimum requirement</li>
                <li>Have profile registered with IRCC’s express entry system</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Nova Scotia Labour Market Priorities</strong></h4>
            <p class="font-size-4 py-1">
                Via this stream, NS PNP is able to nominate the skilled express entry candidates who are able to meet the labour market needs. To be eligible to apply, candidates must:
            </p>
            <ul class="font-size-4">
                <li>Receive a Letter of Interest (LOI) from NSNP within the express entry system</li>
                <li>Submit application within 30 calendar days on which the LOI was issued</li>
                <li>Meet the minimum work experience experiment requirement for express entry stream</li>
                <li>Demonstrate sufficient funds to successfully establish themselves and accompanying family members and pay immigration costs and travel expenses</li>
                <li>Have legal status in current country of residence</li>
                <li>Meet eligibility criteria in place at the time of LOI issuance</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Skilled Worker Stream</strong></h4>
            <p class="font-size-4 py-1">
                NSNP via this stream nominates the skilled immigrants who have received a job offer from an eligible province employer. Immigrants in skilled, semi-skilled, and low-skilled occupations may be eligible to apply. To apply candidate must:
            </p>
            <ul class="font-size-4">
                <li>Have a full-time permanent job offer from a province employer</li>
                <li>Have 1 year of work experience that is related to the job</li>
                <li>Age group must be between 21 to 55 years old</li>
                <li>Have a high school diploma</li>
                <li>Have the appropriate training, skills and/or accreditation for the job</li>
                <li>For a skilled worker, minimum CLB level 5 is required. For semi-skilled and low-skilled, minimum CLB level 4 is required</li>
                <li>Show enough financial resources to successfully settle in the province of Nova Scotia</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Entrepreneur Stream</strong></h4>
            <p class="font-size-4 py-1">
                This stream is for experienced business owners or senior business managers who want to live in Nova Scotia. They must start a new business or buy an existing business and must actively participate in the day-to-day management of the business. After operating the business for a year, the entrepreneur may be nominated for permanent resident status. Application to the stream is by invitation only.
            </p>
            <ul class="font-size-4">
                <li>Must be minimum 21 years of age or older</li>
                <li>Must have intention to live permanently in the province while owning and actively managing a Nova Scotia business</li>
                <li>Have a net worth of at least $600,000 CAD</li>
                <li>Be able to make a personal investment of $150,000 CAD to establish a business in the province</li>
                <li>Must have a minimum of 3 years of work experience for actively managing and owning a business (1/3rd ownership minimum) OR more than 5 years of work experience in a senior business management role</li>
                <li>Have a score of at least 5 on CLB in speaking, listening, reading and writing in either English or French</li>
                <li>Complete an online Expression of Interest (EOI)</li>
                <li>Receive an Invitation to Apply (ITA) to apply from Nova Scotia Office of Immigration</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>International Graduate Entrepreneur Stream</strong></h4>
            <p class="font-size-4 py-1">
                This stream is open for graduates who have completed a minimum of two years of full-time study from a recognized Nova Scotia university or Nova Scotia Community College, have operated their own business in Nova Scotia for at least one year, and intend to settle permanently via Nova Scotia process . To apply candidate must:
            </p>
            <ul class="font-size-4">
                <li>Want to live permanently in province while owning and managing a provincial business</li>
                <li>Have a minimum of one year’s continuous experience actively managing and owning current business (33.3% ownership minimum)</li>
                <li>Have completed a degree or diploma after at least 2 full-time academic year, in person study at a university in the province of Nova Scotia community college</li>
                <li>Have a valid post-graduation work permit</li>
                <li>Must have minimum CLB level 7 in speaking, reading, writing and listening in either English or French</li>
                <li>Completed an online Expression of Interest</li>
                <li>Receive an Invitation to Apply (ITA) from Nova Scotia Office of Immigration</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Physician Stream</strong></h4>
            <p class="font-size-4 py-1">
                This stream is open to internationally-trained general practitioners, family physicians and specialist physicians with a signed employment offer from Nova Scotia’s healthcare authorities. To apply candidate must:
            </p>
            <ul class="font-size-4">
                <li>Want to live in the province permanently while owning and managing a Nova Scotia business</li>
                <li>Have minimum one year’s continuous experience actively managing and owning current provincial business (33.3% ownership minimum)</li>
                <li>Have completed a degree or diploma after minimum 2 academic year of full-time, in-person study at Nova Scotia university or Nova Scotia Community College</li>
                <li>Have a valid post-graduation work permit</li>
                <li>Must have minimum CLB level 7 in reading, listening, speaking and writing in either English or French</li>
                <li>Receive an ITA from Nova Scotia Office of Immigration</li>
            </ul>

            <h4 class="font-size-4 color-font-black"><strong>Physician Stream</strong></h4>
            <p class="font-size-4 py-1">
                This stream is open to internationally-trained general practitioners, family physicians and specialist physicians with a signed employment offer from Nova Scotia’s healthcare authorities. To apply candidate must:
            </p>
            <ul class="font-size-4">
                <li>Want to live in the province permanently while owning and managing a Nova Scotia business</li>
                <li>Have minimum one year’s continuous experience actively managing and owning current provincial business (33.3% ownership minimum)</li>
                <li>Have completed a degree or diploma after minimum 2 academic year of full-time, in-person study at Nova Scotia university or Nova Scotia Community College</li>
                <li>Have a valid post-graduation work permit</li>
                <li>Must have minimum CLB level 7 in reading, listening, speaking and writing in either English or French</li>
                <li>Receive an ITA from Nova Scotia Office of Immigration</li>
            </ul>

            <h2 class="font-size-2 color-font-black py-2"><strong>What are Nova Scotia PNP program requirements?</strong></h2>
            <p class="font-size-4 py-1">
                As we have discussed above, every streams/categories of Nova Scotia Provincial Nominee Program (NS PNP) has their own requirements that needs to fulfill in order to get nominated by the Nova Scotia’s Immigration Office. We have generalized the requirements that are occurring theme for all the streams. These requirements are as follows:
            </p>
            <ul class="font-size-4">
                <li>Candidate’s field of work must be mentioned in occupation in-demand list of the province</li>
                <li>Age group must be between 18 to 49 years</li>
                <li>Must be able to attain a minimum 4.5 in International English Language Testing System (IELTS) or CLB level 4 on language ability</li>
                <li>Must have minimum 1 year of work experience in a relevant occupational field</li>
                <li>Must possess an Educational Credential Assessment (ECA) for qualification diploma/certificate to Canadian education system</li>
                <li>Must be able to show an intention to live in Nova Scotia</li>
            </ul>

            <h2 class="font-size-2 color-font-black py-2"><strong>Occupation in Demand</strong></h2>
            <p class="font-size-4 py-1">
                The Occupations in Demand stream targets specific NOC C, intermediate-skilled occupations that are in high labour market demand in Nova Scotia. The Office of Immigration identifies occupations in demand based on labour market information and eligible occupations are subject to change.
            </p>
            <h4 class="font-size-4 color-font-black"><strong>ELIGIBILITY</strong></h4>
            <p class="font-size-4 py-1">
                To apply you must:
            </p>
            <ul class="font-size-4">
                <li>have a full-time permanent job offer from a Nova Scotia employer in either NOC 3413 (Nurse aides, orderlies and patient service associates) or NOC 7511 (Transport truck drivers)</li>
                <li>have 1 year of work experience related to the job</li>
                <li>be 21 to 55 years old</li>
                <li>have a high school diploma</li>
                <li>have the appropriate training, skills and/or accreditation for the job</li>
                <li>prove language ability equal to Canadian Language Benchmark (CLB) Level 4 even if your first language is English or French</li>
                <li>show enough financial resources to successfully settle in Nova Scotia</li>
            </ul>
