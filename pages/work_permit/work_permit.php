            <p class="font-size-4 py-1">
                Moreover, the ESDC will also review the salary and working conditions to determine that they are consistent with the current labour market in the area.
            </p>

            <p class="font-size-4 py-1">The ESDC will also consider whether hiring a foreign worker will result in a skills transfer to Canadians, whether a union is present at the workplace (if the worker will join the union) and whether hiring a foreign worker will affect a labour dispute at a workplace</p>

            <p class="font-size-4 py-1">If the ESDC officer concludes that no Canadian is available for the advertised by the Canadian employer, the job offer will be validated by a confirmation letter</p>

            <p class="font-size-4 py-1">The confirmation is then signed by the employer and ESDC. This confirmation is then sent by ESDC to the appropriate Visa office where the foreign worker has applied (or will soon apply) for a work permit.  A copy should also be included in the work permit application</p>

            <p class="font-size-4 py-1">The employer is notified that the confirmation has been sent to the Visa office</p>

            <p class="font-size-4 py-1">This completes ESDC’s involvement in the work permit process. The next step is for Citizenship and Immigration Canada to review and approve (or deny) the foreign worker’s work permit application in the applicable foreign visa office</p>

            <p class="font-size-4 py-1">On receipt of positive LMIA from ESDC by the employer, the Canadian employer will provide a temporary job offer to the foreign worker. This detailed job offer together with positive LMIA will be sent by Canadian Employer to foreign worker.  Then the foreign worker will apply to Canadian Consulate in his country of residence or CBSA if the foreign worker applies for work permit at Port of Entry.   A work permit granted for a specific job in Canada will be tied to that job and will be employer specific.  In employer specific work permit, the foreign worker will only work for the specified employer.  However, if such foreign worker finds different employment, he/she is required to get new work permit before performing another job in Canada</p>

            <p class="font-size-4 py-1">The main advantage of a Canadian Work Permit is that it gets you here fast. Once in Canada, you may bring your spouse and dependent children in Canada as they may be eligible for an open work permit</p>




