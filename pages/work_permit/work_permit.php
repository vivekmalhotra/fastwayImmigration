            </p>

            <h3 class="font-size-3 color-font-black"><strong>Eligibility Requirements if you apply from inside Canada</strong></h3>
            <p class="font-size-4 py-1">
                You can only apply for a work permit from inside Canada if:
            </p>
            <ul class="font-size-4">
                <li>you have a valid study or work permit</li>
                <li>your spouse, common-law partner or parents have a valid study or work permit</li>
                <li>you’re eligible for a post-graduation work permit and your study permit is still valid</li>
                <li>you have a temporary resident permit that is valid for six months or more</li>
                <li>you’re waiting on a decision on an application for permanent residence from inside Canada</li>
                <li>you made a claim for refugee protection</li>
                <li>you’ve been recognized as a convention refugee or protected person by the Immigration and Refugee Board of Canada</li>
                <li>you’re allowed to work in Canada without a work permit but you need a work permit to work in a different job (this does not apply to business visitors), or</li>
                <li>you’re a trader, investor, intra-company transferee or professional under the Canada – United States – Mexico Agreement (CUSMA)</li>
            </ul>
            <p class="font-size-4 py-1">
                Each year hundred thousand of people from around the world come to work in Canada on Temporary Work Permits. Generally, a worker from the foreign country needs Canadian Work Permit to take a job in Canada. Canadian Work Permit is permission to work in Canada. This permission can be employer specific or open.
            </p>
            <p class="font-size-4 py-1">
                In most cases, employers are required to apply for a Labour Market Impact Assessment (LMIA) before hiring foreign workers. They have to apply to Employment and Social Development Canada (ESDC) to get LMIA. They can submit application for LMIA before identifying the Temporary Foreign Worker. The Canadian employer must prove to the ESDC that there is no Canadian or permanent resident worker is available to do the specified job thus necessitating hiring of a foreign worker to do that specific job. The application fee for LMIA requests is now $1000 per worker. Under the new LMIA system, classification of jobs has been simplified and jobs are divided into just two categories: high-wage and low-wage.
            </p>
            <p class="font-size-4 py-1">
                An application for LMIA from employer among other things requires the following information :
            </p>

            <ul class="font-size-4">
                <li>Job title & description of job responsibilities, job requirements education, experience or any other specific skills</li>
                <li>Salary, benefits, hours of work, vacations</li>
                <li>Employer’s workplace information number of employees and number of foreign workers already employed</li>
                <li>Evidence of efforts made by the employers to hire Canadian citizens or permanent residents like posting in newspapers, electronic job boards etc.</li>
                <li>Employer should also provide the information:
                    <ul>
                        <li>how a foreign worker will transfer skills and knowledge to Canadians</li>
                        <li>how a foreign worker will create job opportunities for Canadians</li>
                    </ul>
                </li>
                <li>Whether hiring a foreign worker will affect labour dispute, if any</li>
            </ul>
            <p class="font-size-4 py-1">
                ESDC considers several factors while reviewing an application for LMIA from employer like:
            </p>
            <ul class="font-size-4">
                <li>If employment of foreign worker is likely to result in direct job creation or job retention for Canadian citizens or permanent residents</li>
                <li>If it is likely to result in creation or transfer of skills and knowledge for the benefit of Canadian citizens or permanent residents</li>
                <li>If it will fill a labor shortage</li>
                <li>If wages offered are consistent with the prevailing wage rate for the occupation, and working conditions meet generally accepted Canadian standards</li>
                <li>If employer has made, or has agreed to make, reasonable efforts to hire or train Canadian citizens or permanent residents</li>
            </ul>

            <h3 class="font-size-3 color-font-black"><strong>ESDC Decision-making process</strong></h3>
            <p class="font-size-4 py-1">
                The ESDC decision-making process depends on the circumstances.
            </p>
            <p class="font-size-4 py-1">
                For instance, in times of low unemployment and labour shortages, the extent to which an employer must advertise a position will be lower than for areas with high unemployment and labour surpluses.
            </p>
            <p class="font-size-4 py-1">
                In addition, depending on the position, the ESDC may insist on national advertising to attract Canadian candidates for senior level positions.
            </p>
