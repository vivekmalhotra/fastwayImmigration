        <!-- Paragraphs of content -->
        <div class="container py-3 text-justify">
            <p class="font-size-4 py-1">
                If you have lost your status and are still in Canada and your permit expired before you applied for a new work permit, you can apply to restore your status if you have met the conditions listed on your permit.  But you must apply within 90 days of losing your status and keep meeting the requirements for your status.   You may stay in Canada while a decision is made on your application. But you are not allowed to work until your status has been restored.
            </p>
            <p class="font-size-4 py-1">
                If a temporary resident has lost their status or let their authorization to work or study expire, they may apply to restore that status.
            </p>
            <p class="font-size-4 py-1">
                <strong class="color-font-black">It is important to note that an applicant can only restore to an authorization that they held immediately prior to the restoration application.</strong> As an example, a student who lost their status cannot apply to restore status with authorization as a worker.
            </p>
            <p class="font-size-4 py-1">
                <strong>Note:</strong> Section R182 only applies if the temporary resident:
            </p>
            <ul class="font-size-4 mb-1">
                <li>has not been out of status for more than 90 consecutive days</li>
                <li>has not failed to comply with conditions other than paragraph R185(a), any of subparagraphs R185(b)(i) to (iii) or paragraph R185(c)</li>
                <li>is not subject of a declaration under subsection A22.1(1)</li>
            </ul>

            <p class="font-size-4 py-1 mb-1">
                <strong>All in-Canada visitor record, study permit and work permit applications must be submitted electronically.</strong> There are exceptions for specific programs that are not available in the online process.
            </p>

            <h3 class="font-size-3 color-font-black py-2"><strong>Eligibility requirements for restoration of status</strong></h3>
            <h4 class="font-size-4 color-font-black py-1"><strong>Applicant requirements</strong></h4>
            <p class="font-size-4 py-1 mb-1">
                The applicant must:
            </p>
            <ul class="font-size-4 mb-1">
                <li>apply within 90 days of having lost their status</li>
                <li>meet the initial requirements for their stay</li>
                <li>remain in Canada until a decision is made</li>
                <li>have not failed to comply with any condition imposed automatically by regulation [R183] or by an officer [R185], other than those stated below</li>
                <li>have lost their status only because they have failed to comply with any of the following conditions imposed by an officer:
                    <ul>
                        <li>paragraph R185(a) The period authorized for their stay.</li>
                        <li>subparagraphs R185(b)(i) to (iii) The work that they are permitted to engage in, or are prohibited from engaging in, in Canada, including the
                            <ul>
                                <li>type of work</li>
                                <li>employer</li>
                                <li>location of work</li>
                            </ul>
                        </li>
                        <li>paragraph R185(c) The studies that they are permitted to engage in, or are prohibited from engaging in, in Canada, including the
                            <ul>
                                <li>type of studies or course</li>
                                <li>educational institution</li>
                                <li>location of the studies</li>
                                <li>times and periods of the studies</li>
                            </ul>
                        </li>
                    </ul>
                </li>
                <li>continue to meet the requirements of a temporary resident and the requirements of the work or study permit, as applicable</li>
            </ul>
            <h4 class="font-size-4 color-font-black py-1"><strong>Ninety-day period</strong></h4>
            <ul class="font-size-4 mb-1">
                <li>Foreign nationals have 90 days from the date they lose their temporary resident status to apply for restoration. Under section A47, status is lost on expiry of the period of authorized stay or on determination by an officer that the foreign national has failed to comply with any other requirement of the IRPA</li>
                <ul>
                    <li>under section A47,
                        <ul>
                            <li>a period of authorized stay includes periods of status imposed by law (implied status) under subsection R183(5)</li>
                            <li>if an application is rejected under section R12 as incomplete, there is no extension of the period of authorized stay (implied status)</li>
                        </ul>
                    </li>
                    <li>under paragraph R222(1)(a), a study permit becomes invalid 90 days after the student completes their program of study</li>
                </ul>
                <li>If an application for an extension of temporary resident status under section R181 is refused, the foreign national has 90 days from the date of the refusal notice to apply for restoration, if otherwise eligible</li>
                <li>If an application for an extension is received after the temporary resident status is lost, the case processing centre should refuse the application and respond with a warning notice to the applicant that they are eligible to apply for restoration</li>
                <li>Foreign nationals have 90 days from the date their status expired to submit their restoration application and pay the corresponding fee</li>
            </ul>

            <h4 class="font-size-4 color-font-black py-1"><strong>Continuation of work or study</strong></h4>
            <p class="font-size-4 py-1">
                Foreign nationals applying for restoration have lost their status and may not continue to work or attend school until their status has been restored and a new work or study permit has been issued
            </p>

            <h4 class="font-size-4 color-font-black py-1"><strong>Leaving Canada</strong></h4>
            <p class="font-size-4 py-1">
                Restoration of status cannot be granted at the port of entry.
            </p>
            <p class="font-size-4 py-1">
                If a foreign national leaves Canada, they will be deemed to be seeking a <strong>new entry</strong> on their return, and the previous non-compliance with imposed conditions may make them inadmissible to Canada as per section A41 and subsection A29(2)
            </p>
            <p class="font-size-4 py-1">
                Foreign nationals who have failed to comply with the conditions imposed under section R185 need to apply in Canada for restoration of their status or leave Canada immediately.
            </p>
            <p class="font-size-4 py-1">
                During the processing of a restoration application, should an officer determine that the foreign national has left Canada, the officer shall refuse the application as the foreign national is no longer eligible to apply for restoration. In this situation, processing fees are not refunded.
            </p>

            <h4 class="font-size-4 color-font-black py-1"><strong>Possible restoration scenarios</strong></h4>
            <p class="font-size-4 py-1">
                A foreign national <strong>can only</strong> apply to restore their status and authorization to one they held immediately prior to the need to apply for restoration.
            </p>
            <p class="font-size-4 py-1">
                For example, a temporary resident with authorization to study who is out of status cannot apply to restore their temporary resident status with authorization to work. They must apply to restore their temporary resident status with authorization to study. If eligible, they can also apply for a work permit in the same application package. Officers will process the restoration of the temporary resident status and authorization to study. If approved, the work permit application will be processed.
            </p>

            <h4 class="font-size-4 color-font-black py-1"><strong>Students</strong></h4>
            <p class="font-size-4 py-1">
                A temporary resident who held a study permit and has lost their status can apply in Canada for restoration of their temporary resident status and study permit. They may also apply and pay for a work permit if they meet the requirements of the work permit program they are applying for.
            </p>

            <h4 class="font-size-4 color-font-black py-1"><strong>Workers</strong></h4>
            <p class="font-size-4 py-1">
                A temporary resident who held a work permit and lost their status can apply in Canada for restoration of their temporary resident status and authorization to work within 90 days of having lost their status. In addition to applying for restoration and a new work permit, they may also apply for a study permit, and pay the fees, if they meet the requirements for study permit issuance.
            </p>

            <h4 class="font-size-4 color-font-black py-1"><strong>Visitors</strong></h4>
            <p class="font-size-4 py-1">
                A visitor who is out of status may apply to restore their status as a temporary resident. Temporary residents who are eligible to apply in Canada for a work or study permit [R199 or R215] may do so when restoring their temporary resident status. The fees for the study or work permit must be paid in addition to the restoration fee, unless they are otherwise exempt.
            </p>

            <h4 class="font-size-4 color-font-black py-1"><strong>Temporary Resident Permit (TRP) holders</strong></h4>
            <p class="font-size-4 py-1">
                A temporary resident permit (TRP) holder who has let their permit expire is not eligible for restoration. They must submit an application for a new TRP.
            </p>
            <p class="font-size-4 py-1">
                <strong>Note:</strong> The person must still satisfy the officer that they are a genuine temporary resident and meet all the requirements of the IRPA in order to qualify for restoration.
            </p>


            <h4 class="font-size-4 color-font-black py-1"><strong>Restoration Fees</strong></h4>
            <p class="font-size-4 py-1">
                Foreign nationals applying for restoration must pay all the corresponding fees. Any foreign national applying to restore as a temporary resident must pay only the restoration fee (that is, $200). However, if the foreign national also requires a work or study permit, they must pay the cost recovery fees for each permit <strong>in addition</strong> to the fee for restoration (that is, $200 plus each permit fee), unless they are otherwise exempt.
            </p>
            <p class="font-size-4 py-1">
                As an example, a study permit holder who is applying to restore their study permit must submit the fees for both restoration and a study permit. If they wish to apply for a work permit in addition to restoration and the study permit, they must also pay the work permit fee ($155). The officer first evaluates the restoration application and, if approved, processes any application for a study or work permit.
            </p>

            <h3 class="font-size-3 color-font-black py-2"><strong>Process at the Case Processing Centre in Edmonton (CPC-E)</strong></h3>
            <h4 class="font-size-4 color-font-black"><strong>Step 1</strong></h4>
            <p class="font-size-4 py-1">
                The Case Processing Centre in Edmonton (CPC-E) receives the restoration application and ensures that all the required documentation and fees (restoration fee plus any permit fee, if applicable) are included.
            </p>
            <p class="font-size-4 pb-1">
                <strong>Note:</strong> For students applying for a post-graduation work permit (PGWP), please refer to the PGWP Program application process.
            </p>

            <h4 class="font-size-4 color-font-black"><strong>Step 2</strong></h4>
            <p class="font-size-4 py-1">
                The officer assesses the applicant’s eligibility for restoration:
            </p>
            <p class="font-size-4 mb-0">
                <strong>If the applicant is eligible for restoration, the officer</strong>
            </p>
            <ul class="font-size-4 mb-1">
                <li>proceeds with the assessment of the application (medical results, bona fides, etc.)</li>
                <li>issues medical instructions if a medical examination is required by mailing the following to the client:
                    <ul>
                        <li>medical instructions</li>
                        <li>a medical examination form [IMM 1017]</li>
                        <li>panel physician list</li>
                    </ul>
                </li>
            </ul>
            <p class="font-size-4 pb-1">
                If all requirements are met, including medical examination results, the officer issues a visitor record (or appropriate permit) outlining the conditions for the restoration of status. The document is mailed to the client.
            </p>

            <p class="font-size-4 mb-0">
                <strong>If the applicant is not eligible for restoration, the officer</strong>
            </p>
            <ul class="font-size-4 mb-1">
                <li>refuses the application, and the applicant is notified in writing that they must leave Canada immediately</li>
                <li>determines whether a section 44 report may be warranted due to a possible violation of the IRPA or IRPR
                    <ul>
                        <li>writes case notes detailing the violation</li>
                        <li>refers the application to a Domestic Network (DN) office near the applicant’s place of residence for further assessment and possible interview</li>
                    </ul>
                </li>
            </ul>

            <h3 class="font-size-3 color-font-black py-2"><strong>Process at DN office (if applicable)</strong></h3>
            <p class="font-size-4 pb-1">
                If CPC-E refers the restoration application to a local office, the DN officer may call the applicant in for an interview to gather additional information. At or following the interview, or upon review of the application, the officer will either approve or refuse the application.
            </p>
            <ul class="font-size-4 mb-1">
                <li><strong>If the application is approved and restoration is granted,</strong> a visitor record or appropriate permit outlining the new conditions is issued to the applicant</li>
                <li><strong>If restoration is refused,</strong> the decision is communicated to the applicant. A letter is given to the applicant notifying them of the refusal and the reasons for it</li>
            </ul>
